    private void jBRefreshUsersActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBRefreshUsersActionPerformed

        try {
            ResultSet resp = (ResultSet) returnAllUsers();
            LtpUtil.loadFormatJTable(jScrollUsers, resp);
            jTextId.setText("");
            jTextIp.setText("");
            jTextName.setText("");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } catch (LtpUtilException ex) {
            Logger.getLogger(home.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jBRefreshUsersActionPerformed

    private void showAllVips() {
        try {
            ResultSet resp = (ResultSet) returnAllVips();
            LtpUtil.loadFormatJTable(jScrollVips, resp, true);
        } catch (SQLException | LtpUtilException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }

