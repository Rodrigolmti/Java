    private void jTableVipsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableVipsMouseClicked

        int row = jTableVips.getSelectedRow();

        String name = String.valueOf(jTableVips.getValueAt(row, 0));
        String groupVip = String.valueOf(jTableVips.getValueAt(row, 2));
        String sapphireTime = String.valueOf(jTableVips.getValueAt(row, 3));
        String esmeraldaTime = String.valueOf(jTableVips.getValueAt(row, 4));
        String diamanteTime = String.valueOf(jTableVips.getValueAt(row, 5));

        jTNick.setText(name);
        jTGorup.setText(groupVip);
        jTDays.setText(sapphireTime);
        jTDaysEsm.setText(esmeraldaTime);
        jTDaysDiam.setText(diamanteTime);

        vip = new Vips(groupVip, sapphireTime, esmeraldaTime, diamanteTime, name);
    }//GEN-LAST:event_jTableVipsMouseClicked

    private void jBEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEditActionPerformed

        String sapphireTime = jTDays.getText();
        String esmeraldaTime = jTDaysEsm.getText();
        String diamanteTime = jTDaysDiam.getText();
        String name = jTNick.getText();

        vip.setNome(name);
        vip.setSapphireTime(sapphireTime);
        vip.setEsmeraldaTime(esmeraldaTime);
        vip.setDiamanteTime(diamanteTime);

        JOptionPane.showMessageDialog(this, "Usu√°rio editado com sucesso!");

        try {
            updateVip(vip);
        } catch (SQLException ex) {
            Logger.getLogger(home.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jBEditActionPerformed

