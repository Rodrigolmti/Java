    private void jTableUsersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableUsersMouseClicked

        int row = jTableUsers.getSelectedRow();
        String id = String.valueOf(jTableUsers.getValueAt(row, 0));
        String name = String.valueOf(jTableUsers.getValueAt(row, 1));
        String ip = String.valueOf(jTableUsers.getValueAt(row, 2));

        jTextId.setText(id);
        jTextName.setText(name);
        jTextIp.setText(ip);

    }//GEN-LAST:event_jTableUsersMouseClicked

